public class Insertion {
    public int idCandidat;
    public int nombresdevotes;

    public Insertion(int idCandidat, int nombresdevotes) {
        this.idCandidat = idCandidat;
        this.nombresdevotes = nombresdevotes;
    }

    public int getIdCandidat() {
        return idCandidat;
    }

    public void setIdCandidat(int idCandidat) {
        this.idCandidat = idCandidat;
    }

    public int getNombresdevotes() {
        return nombresdevotes;
    }

    public void setNombresdevotes(int nombresdevotes) {
        this.nombresdevotes = nombresdevotes;
    }
}
